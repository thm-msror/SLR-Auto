def generate_title_abstract_keywords(
    introduction: str,
    methodology: str,
    results: str,
    discussion: str,
    conclusion: str,
    prompt_text: str = ABSTRACT_PROMPT,
    model_name: Optional[str] = None,
    temperature: float = 0.2,
    max_output_tokens: int = 700,
) -> Dict[str, object]:
    cleaned_inputs = {
        "introduction": (introduction or "").strip(),
        "methodology": (methodology or "").strip(),
        "results": (results or "").strip(),
        "discussion": (discussion or "").strip(),
        "conclusion": (conclusion or "").strip(),
    }

    missing = [name for name, value in cleaned_inputs.items() if not value]
    if missing:
        raise ValueError(f"Missing required inputs: {', '.join(missing)}")

    raw = call_gpt_chat(
        messages=[
            {"role": "system", "content": (prompt_text or "").strip() or ABSTRACT_PROMPT},
            {
                "role": "user",
                "content": (
                    "Introduction:\n"
                    f"{cleaned_inputs['introduction']}\n\n"
                    "Methodology:\n"
                    f"{cleaned_inputs['methodology']}\n\n"
                    "Results and Findings:\n"
                    f"{cleaned_inputs['results']}\n\n"
                    "Discussion and Future Work:\n"
                    f"{cleaned_inputs['discussion']}\n\n"
                    "Conclusion:\n"
                    f"{cleaned_inputs['conclusion']}"
                ),
            },
        ],
        model_name=model_name,
        temperature=temperature,
        max_tokens=max_output_tokens,
    )

    parsed = parse_title_abstract_keywords(raw)
    parsed["raw"] = raw.strip()
    return parsed


def parse_title_abstract_keywords(text: str) -> Dict[str, object]:
    cleaned = (text or "").strip()
    cleaned = re.sub(r"```[a-zA-Z]*\n?", "", cleaned)
    cleaned = cleaned.replace("```", "").strip()

    title_match = re.search(r"TITLE:\s*(.+)", cleaned, re.IGNORECASE)
    abstract_match = re.search(
        r"ABSTRACT:\s*(.*?)(?:\n\s*KEYWORDS\s*:|\Z)",
        cleaned,
        re.IGNORECASE | re.DOTALL,
    )
    keywords_match = re.search(r"KEYWORDS\s*:\s*(.*)$", cleaned, re.IGNORECASE | re.DOTALL)

    title = title_match.group(1).strip() if title_match else ""
    abstract = abstract_match.group(1).strip() if abstract_match else ""
    keywords_block = keywords_match.group(1).strip() if keywords_match else ""
    keywords = _parse_keywords(keywords_block)

    if not title:
        lines = [line.strip() for line in cleaned.splitlines() if line.strip()]
        if lines:
            title = re.sub(r"^(TITLE:\s*)", "", lines[0], flags=re.IGNORECASE).strip()

    if not abstract:
        raise ValueError("Could not parse abstract from GPT response.")
    if not title:
        raise ValueError("Could not parse title from GPT response.")

    return {
        "title": title,
        "abstract": abstract,
        "keywords": keywords,
    }


def _parse_keywords(text: str) -> List[str]:
    if not text:
        return []

    lines = [line.strip() for line in text.splitlines() if line.strip()]
    keywords: List[str] = []
    for line in lines:
        cleaned = re.sub(r"^\s*(?:[-*•]|\d+[.)])\s*", "", line).strip()
        if not cleaned:
            continue
        keywords.append(cleaned)

    if not keywords and "," in text:
        keywords = [item.strip() for item in text.split(",") if item.strip()]

    deduped: List[str] = []
    seen = set()
    for keyword in keywords:
        if keyword not in seen:
            seen.add(keyword)
            deduped.append(keyword)
    return deduped


